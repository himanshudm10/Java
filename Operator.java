package Day2;

public class Operator
{

	public static void main(String[] args) 
	{
	
		int a=10, b=12;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		boolean res=(a>b);
		System.out.println(res);
	}

}
